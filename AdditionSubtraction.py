print("This is a program to show the addition and subtraction of two numbers.")

first_num = int(input("Enter the first number:"))
second_num = int(input("Enter the second number:"))

print("You entered", first_num, "as the first number and", second_num, "as the second number")
print("Addition of the two numbers, i.e.,",first_num, "+", second_num, "is", first_num + second_num)
print("Subtraction of the two numbers, i.e.,", first_num, "-", second_num, "is", first_num - second_num)

